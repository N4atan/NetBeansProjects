    private void BtnAtacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAtacarActionPerformed
        // TODO add your handling code here:
		int dano = player.time.get(0).getForca() - (pokemonOponente.getDefense() / 4);
		
		// Garante que o dano mÃ­nimo seja 1
		if (dano < 1) {
			dano = 1;
		}
				
		pokemonOponente.setHp(pokemonOponente.getHp() - dano);
		
		if(pokemonOponente.getHp() > 0) {
			StatusBattleTxtArea.setText("VocÃª atacou! e causou de dano: " + dano);
		} else {
			StatusBattleTxtArea.setText("VocÃª venceu!");
			BtnAtacar.setEnabled(false);
			BtnFugir.setEnabled(false);
		}
		
		LblHpOponPokemon.setText(String.valueOf(pokemonOponente.getHp()));
    }//GEN-LAST:event_BtnAtacarActionPerformed

