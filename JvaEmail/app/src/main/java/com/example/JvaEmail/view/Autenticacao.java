    private void titlePassword3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_titlePassword3MouseClicked
		// TODO add your handling code here:
		ArrayList<JTextComponent> list = new ArrayList();
		list.add(inputEmailL);

		if (checkAnyEmpty(list)) {
			JOptionPane.showMessageDialog(this, "Preencha com seu email.\nApós preencher, clique aqui novamente.", "Instruções", JOptionPane.QUESTION_MESSAGE);
			return;
		}

		boolean respC = this.usuarioController.checkExists(inputEmailL.getText().trim());

		if (this.usuarioController.hasError()) {
			JOptionPane.showMessageDialog(this, this.usuarioController.getError(), "Ops...", JOptionPane.ERROR_MESSAGE);
			return;
		}

		if (!respC) {
			JOptionPane.showMessageDialog(this, "O email que você digitou não está cadastrado!", "Verifique seu email", JOptionPane.ERROR_MESSAGE);
		} else {
			String novaSenha = JOptionPane.showInputDialog(this, "Digite sua nova senha: \n- Não se preocupe, ela não será visível no banco de dados.", "Redefinir Senha", JOptionPane.PLAIN_MESSAGE);

			if (!novaSenha.equals("")) {
				String respUpd = this.usuarioController.atualizarSenha(inputEmailL.getText().trim(), novaSenha.trim());

				if (usuarioController.hasError()) {
					JOptionPane.showMessageDialog(this, this.usuarioController.getError(), "Ops...", JOptionPane.ERROR_MESSAGE);
				} else {
					JOptionPane.showMessageDialog(this, respUpd, "Operação Realizada", JOptionPane.INFORMATION_MESSAGE);
				}
			}
		}
    }//GEN-LAST:event_titlePassword3MouseClicked


