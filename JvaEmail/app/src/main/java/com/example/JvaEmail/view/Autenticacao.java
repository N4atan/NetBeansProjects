    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
		// TODO add your handling code here:
		ArrayList<JTextComponent> list = new ArrayList();
		list.add(inputEmailL);
		list.add(inputPasswordL);

		if (checkAnyEmpty(list)) {
			JOptionPane.showMessageDialog(this, "Por Favor, preencha todos os campos!", "Ops...", JOptionPane.WARNING_MESSAGE);
			return;
		}

		Usuario usuarioLocal = this.usuarioController.login(inputEmailL.getText().trim(),
				String.valueOf(inputPasswordL.getPassword()).trim());

		if (this.usuarioController.hasError()) {
			JOptionPane.showMessageDialog(this, this.usuarioController.getError(), "Ops...", JOptionPane.ERROR_MESSAGE);
		} else {
			JOptionPane.showMessageDialog(this, "Login efetuado com sucesso!", "Bem-Vindo Novamente", JOptionPane.INFORMATION_MESSAGE);
			this.dispose();
			new JvaHome(usuarioController, usuarioLocal).setVisible(true);
		}
    }//GEN-LAST:event_btnLoginActionPerformed

