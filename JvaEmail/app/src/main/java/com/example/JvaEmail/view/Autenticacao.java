    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
		// TODO add your handling code here:
		ArrayList<JTextComponent> list = new ArrayList();
		list.add(inputEmailR);
		list.add(inputName);
		list.add(inputPasswordR);

		if (checkAnyEmpty(list)) {
			JOptionPane.showMessageDialog(this, "Por Favor, preencha todos os campos!", "Ops...", JOptionPane.WARNING_MESSAGE);
			return;
		}

		String respC = usuarioController.registar(inputEmailR.getText().trim(),
				inputName.getText().trim(),
				String.valueOf(inputPasswordR.getPassword()).trim());

		if (usuarioController.hasError()) {
			JOptionPane.showMessageDialog(this, this.usuarioController.getError(), "Ops...", JOptionPane.ERROR_MESSAGE);
		} else {
			JOptionPane.showMessageDialog(this, respC, "Bem-Vindo!", JOptionPane.INFORMATION_MESSAGE);
			inputEmailR.setText("");
			inputName.setText("");
			inputPasswordR.setText("");
		}
    }//GEN-LAST:event_btnCreateActionPerformed

