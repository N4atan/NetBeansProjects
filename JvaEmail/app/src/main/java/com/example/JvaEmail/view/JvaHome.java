    private void tableListaEmailsKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableListaEmailsKeyPressed
		// TODO add your handling code here:
		if (evt.getExtendedKeyCode() == KeyEvent.VK_ENTER) {
			int linhaSelecionda = tableListaEmails.getSelectedRow();
			System.out.println(linhaSelecionda);

			if (linhaSelecionda != -1) {
				String nomeRem = (String) tableListaEmails.getValueAt(linhaSelecionda, 0);
				String tituloEmail = (String) tableListaEmails.getValueAt(linhaSelecionda, 1);

				Email email4open = this.emailController.abrirDetalhes(nomeRem, tituloEmail);

				if (email4open != null) {
					CardLayout cl = (CardLayout) jPanel1.getLayout();
					cl.show(jPanel1, "detalhesCard");

					inputRemDetalhes.setText(email4open.getDestinatario().getEmail());
					inputTituloDetalhes.setText(email4open.getTitulo());
					corpoDetalhes.setText(email4open.getConteudo());
					idEmail.setText("" + email4open.getId());
				}
			}
		}
    }//GEN-LAST:event_tableListaEmailsKeyPressed

