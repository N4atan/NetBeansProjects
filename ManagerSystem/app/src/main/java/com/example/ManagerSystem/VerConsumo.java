	private void atualizarLista(){
		jLabelR$Total.setText("Total: R$ " + mesaAtual.valorTotal);
		
		// Cria um modelo de lista para a JList (substitui os dados antigos)
		DefaultListModel<String> listModel = new DefaultListModel<>();
		
		for (Produto produto : mesaAtual.itensConsumidos) {
			listModel.addElement(produto.toString()); // Adiciona a descri√ß√£o do produto no modelo
		}
		
		jListItensConsumidos.setModel(listModel); // Atualiza o modelo da lista
	}
	
	private void adicionarItens(){
		for(Produto produto : telaAnterior.itensDisponiveis){
			addPanelProduto(produto);
		}
	}
	
	private void addPanelProduto(Produto produto){
		// Cria um painel para o produto
		JPanel produtoPanel = new JPanel();
		produtoPanel.setLayout(new GridLayout(2, 1)); // 2 linhas, 1 coluna
		produtoPanel.setBorder(BorderFactory.createLineBorder(Color.decode("#1c140d")));
		produtoPanel.setPreferredSize(new Dimension(117, 100));  // Ajuste a altura conforme necess√°rio
		produtoPanel.setMinimumSize(new Dimension(117, 100));  // Ajuste a altura conforme necess√°rio
		produtoPanel.setMaximumSize(new Dimension(117, 100));  // Ajuste a altura conforme necess√°rio
		produtoPanel.setBackground(Color.WHITE);

		// Cria um JLabel para o nome do produto (com quebra de linha)		
		JLabel nomeDoProduto = new JLabel(produto.nome);
		nomeDoProduto.setHorizontalAlignment(SwingConstants.CENTER);  // Centraliza o texto
		produtoPanel.add(nomeDoProduto);

		// Adiciona o pre√ßo do produto
		JLabel precoDoProduto = new JLabel("R$ " + String.format("%.2f", produto.valor));
		precoDoProduto.setHorizontalAlignment(SwingConstants.CENTER);  // Centraliza o texto horizontalmente
		produtoPanel.add(precoDoProduto);

		// Adiciona um MouseAdapter para capturar eventos de clique
		produtoPanel.addMouseListener(new MouseAdapter() {
			// Defina os eventos aqui, caso necess√°rio
			@Override
			public void mouseEntered(MouseEvent e) {
				// Muda a cor ao passar o mouse sobre o painel
				produtoPanel.setBackground(Color.decode("#f2e9e1"));
			}

			@Override
			public void mouseExited(MouseEvent e) {
				// Restaura a cor ao sair com o mouse
				produtoPanel.setBackground(Color.WHITE);
			}

			@Override
			public void mouseReleased(MouseEvent e) {
				// Restaura a cor ao soltar o clique do mouse
				produtoPanel.setBackground(Color.decode("#f2e9e1"));
			}

			@Override
			public void mousePressed(MouseEvent e) {
				// Muda a cor ao clicar no painel (opcional, se voc√™ quiser)
				produtoPanel.setBackground(Color.decode("#cbe86b"));
				
				mesaAtual.addConsumo(produto);
				atualizarLista();
			}
		});

		// Para adicionar ao jPanel certo
		if (produto.categoria.equalsIgnoreCase("Lanche")) {
			jPanelComidas.add(produtoPanel);
			// Define o tamanho preferido de jPanelBebidas, ajustando a altura com base no n√∫mero de componentes (cada um com 117 de altura)
			jPanelComidas.setPreferredSize(new Dimension(jPanelComidas.getPreferredSize().width, jPanelComidas.getComponentCount() * produtoPanel.getPreferredSize().height));
			jPanelComidas.revalidate();
			jPanelComidas.repaint();
		} else {
			jPanelBebidas.add(produtoPanel);
			// Define o tamanho preferido de jPanelBebidas, ajustando a altura com base no n√∫mero de componentes (cada um com 117 de altura)
			jPanelBebidas.setPreferredSize(new Dimension(jPanelBebidas.getPreferredSize().width, jPanelBebidas.getComponentCount() * produtoPanel.getPreferredSize().height));
			jPanelBebidas.revalidate();
			jPanelBebidas.repaint();
		}
	}
