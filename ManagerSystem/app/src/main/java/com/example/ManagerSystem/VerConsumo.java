    private void jListItensConsumidosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jListItensConsumidosKeyPressed
        // TODO add your handling code here:
	// Verifica se a tecla pressionada √© a tecla DELETE
	if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
		// Obt√©m o √≠ndice do item selecionado
		int selectedIndex = jListItensConsumidos.getSelectedIndex();
			
		if (selectedIndex != -1) { // Verifica se h√° um item selecionado
			// Remove o item do ArrayList da Mesa
			mesaAtual.removerProduto(selectedIndex);
 
		        // Atualiza a JList com os itens restantes
			atualizarLista();
		}
	}
    }//GEN-LAST:event_jListItensConsumidosKeyPressed

