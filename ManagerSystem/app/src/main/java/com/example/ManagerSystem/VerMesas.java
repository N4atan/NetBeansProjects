	private void addMesa(){
		Mesa mesaNova = new Mesa(Integer.parseInt(TxtFieldNumeroDaMesa.getText()));
	
		mesas.add(mesaNova);

		// Cria um painel para a mesa
		JPanel mesaPanel = new JPanel();
		mesaPanel.setLayout(new BorderLayout());
		mesaPanel.setBorder(BorderFactory.createLineBorder(Color.decode("#8FBE00")));
		mesaPanel.setPreferredSize(new Dimension(50, 50));
		//mesaPanel.setBackground(new Color(144, 238, 144)); //Para verde claro e RGB
		mesaPanel.setBackground(Color.decode("#AEE239")); //hexadecimal 

		// Adiciona o n√∫mero da mesa no painel
		JLabel lblMesa = new JLabel(TxtFieldNumeroDaMesa.getText());		
		lblMesa.setHorizontalAlignment(SwingConstants.CENTER);  // Alinha o texto horizontalmente
		lblMesa.setVerticalAlignment(SwingConstants.CENTER);  // Alinha o texto verticalmente
		mesaPanel.add(lblMesa, BorderLayout.CENTER);

		// Adiciona um MouseAdapter para capturar eventos de clique
		mesaPanel.addMouseListener(new MouseAdapter() {
			public void mouseClicked(MouseEvent e) {
				// Exemplo de a√ß√£o ao clicar no painel
				VerConsumo verConsumo = new VerConsumo(mesaNova, VerMesas.this);			
				verConsumo.setVisible(true);
				VerMesas.this.setVisible(false);
			}
		});

		// Adiciona no painel prinicipal
		jPanelMesas.add(mesaPanel);
		jPanelMesas.setPreferredSize(new Dimension(jPanelMesas.getPreferredSize().width, jPanelMesas.getComponentCount() * mesaPanel.getPreferredSize().height));
		jPanelMesas.revalidate();
		jPanelMesas.repaint();
		
		TxtFieldNumeroDaMesa.setText("");
		jDialog1.dispose();
		
		
		
		//Verifica√ß√£o :)
		System.out.println("Mesas dispon√≠veis:");
		for(Mesa mesa : mesas){
			System.out.println(mesa.numeroDaMesa);
		}
		System.out.println("------------------");
	}
	
	private boolean checkMesaHasExist(){
		for(Mesa mesa : mesas){
			if(mesa.numeroDaMesa == Integer.parseInt(TxtFieldNumeroDaMesa.getText())){
				TxtFieldNumeroDaMesa.setForeground(Color.red);
				return true;
			}
		}
		TxtFieldNumeroDaMesa.setForeground(Color.black);
		return false;
	}
	
