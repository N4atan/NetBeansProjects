    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
		// TODO add your handling code here:

		String useremail = inputEmail.getText();
		String userpassword = String.valueOf(inputPassword.getPassword());

		if (useremail.isEmpty() || userpassword.isEmpty()) {
			JOptionPane.showMessageDialog(null, "Preencha corretamente os campos!", "Alert", JOptionPane.WARNING_MESSAGE);
		} else {
			String action = userControler.registerUser(useremail, userpassword);

			if (action != null) {
				JOptionPane.showMessageDialog(
						null,
						action,
						"Error",
						JOptionPane.ERROR_MESSAGE
				);
			} else {
				JOptionPane.showMessageDialog(
						null,
						"Cadastrado com Sucesso!",
						"Success",
						JOptionPane.INFORMATION_MESSAGE
				);

				inputEmail.setText("");
				inputPassword.setText("");
			}
		}
    }//GEN-LAST:event_btnRegisterActionPerformed

